import { spawnSync } from "node:child_process";
import { existsSync } from "node:fs";
import fs from "node:fs/promises";
import os from "node:os";
import path from "node:path";

const workspace = process.cwd();
const configPath = process.env.OVERLEAF_CONFIG || "data/overleaf-projects.json";
const outputJson = process.env.OVERLEAF_OUTPUT_JSON || "data/overleaf.json";
const outputScript = process.env.OVERLEAF_OUTPUT_SCRIPT || "data/overleaf.generated.js";
const token = process.env.OVERLEAF_SYNC_TOKEN || process.env.GH_TOKEN || process.env.GITHUB_TOKEN || "";

function slugify(value) {
  return String(value || "")
    .normalize("NFKC")
    .toLowerCase()
    .replace(/[^\p{Letter}\p{Number}]+/gu, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 80);
}

function run(command, args, options = {}) {
  const result = spawnSync(command, args, { stdio: "inherit", ...options });
  if (result.status !== 0) {
    const redactedArgs = args.map((arg) => (arg.includes("AUTHORIZATION:") ? "http.extraheader=<redacted>" : arg));
    throw new Error(`${command} ${redactedArgs.join(" ")} failed with exit code ${result.status}`);
  }
}

function capture(command, args, options = {}) {
  const result = spawnSync(command, args, { encoding: "utf8", ...options });
  if (result.status !== 0) return "";
  return result.stdout.trim();
}

function resolveInsideWorkspace(relativePath) {
  const absolute = path.resolve(workspace, relativePath);
  if (!absolute.startsWith(`${workspace}${path.sep}`)) {
    throw new Error(`Refusing to write outside workspace: ${relativePath}`);
  }
  return absolute;
}

function githubCloneUrl(repo) {
  if (repo.startsWith("github:")) {
    return `https://github.com/${repo.slice("github:".length).replace(/\.git$/, "")}.git`;
  }
  return repo;
}

function gitCloneArgs(repo, branch, destination) {
  const cloneUrl = githubCloneUrl(repo);
  const args = ["clone", "--depth", "1"];
  if (branch) args.push("--branch", branch);
  args.push(cloneUrl, destination);

  if (!token || !cloneUrl.startsWith("https://github.com/")) return args;

  const credential = Buffer.from(`x-access-token:${token}`).toString("base64");
  return ["-c", `http.https://github.com/.extraheader=AUTHORIZATION: basic ${credential}`, ...args];
}

function pdfNameFromTex(texPath) {
  return `${path.basename(texPath, path.extname(texPath))}.pdf`;
}

async function readConfig() {
  if (!existsSync(configPath)) return { projects: [] };
  return JSON.parse(await fs.readFile(configPath, "utf8"));
}

async function copyPdf(sourcePdf, output) {
  if (!existsSync(sourcePdf)) throw new Error(`PDF was not produced: ${sourcePdf}`);
  const outputPath = resolveInsideWorkspace(output);
  await fs.mkdir(path.dirname(outputPath), { recursive: true });
  await fs.copyFile(sourcePdf, outputPath);
}

async function syncProject(project, rootTemp) {
  if (!project.publish) return null;
  if (!project.repo) throw new Error(`${project.id || project.title}: repo is required`);
  if (!project.output) throw new Error(`${project.id || project.title}: output is required`);

  const id = project.id || slugify(project.title);
  const cloneDir = path.join(rootTemp, id);
  run("git", gitCloneArgs(project.repo, project.branch, cloneDir));

  let sourcePdf = "";
  if (project.build === false) {
    sourcePdf = path.resolve(cloneDir, project.pdf || project.sourcePdf || pdfNameFromTex(project.tex || "main.tex"));
  } else {
    const texPath = path.resolve(cloneDir, project.tex || "main.tex");
    const texDir = path.dirname(texPath);
    const buildDir = path.join(cloneDir, ".build-overleaf");
    await fs.mkdir(buildDir, { recursive: true });
    run(
      "latexmk",
      ["-pdf", "-interaction=nonstopmode", "-halt-on-error", `-outdir=${buildDir}`, path.basename(texPath)],
      { cwd: texDir }
    );
    sourcePdf = path.join(buildDir, project.pdf || pdfNameFromTex(texPath));
  }

  await copyPdf(sourcePdf, project.output);

  const revision = capture("git", ["rev-parse", "HEAD"], { cwd: cloneDir });
  return {
    id,
    title: project.title || id,
    kind: project.kind || "note",
    label: project.label || (project.kind === "paper" ? "PDF" : "Slides"),
    href: project.output.replace(/\\/g, "/"),
    outputName: path.basename(project.output),
    description: project.description || "",
    language: project.language || "",
    year: project.year || "",
    match: project.match || [],
    publish: true,
    source: "overleaf-github",
    sourceRepo: project.sourceLabel || project.repo,
    sourceRevision: revision
  };
}

function escapeScriptJson(json) {
  return json.replace(/</g, "\\u003c").replace(/\u2028/g, "\\u2028").replace(/\u2029/g, "\\u2029");
}

async function writeOutputs(artifacts) {
  const data = {
    source: {
      name: "Overleaf via GitHub",
      description: "Generated by scripts/sync-overleaf-artifacts.mjs"
    },
    artifacts
  };

  await fs.mkdir(path.dirname(resolveInsideWorkspace(outputJson)), { recursive: true });
  await fs.writeFile(resolveInsideWorkspace(outputJson), `${JSON.stringify(data, null, 2)}\n`);
  await fs.writeFile(resolveInsideWorkspace(outputScript), `window.overleafData = ${escapeScriptJson(JSON.stringify(data, null, 2))};\n`);
}

const config = await readConfig();
const projects = Array.isArray(config.projects) ? config.projects : [];
const tempRoot = await fs.mkdtemp(path.join(os.tmpdir(), "homepage-overleaf-"));

try {
  const artifacts = [];
  for (const project of projects) {
    const artifact = await syncProject(project, tempRoot);
    if (artifact) artifacts.push(artifact);
  }
  await writeOutputs(artifacts);
  console.log(`Synced ${artifacts.length} Overleaf artifact(s).`);
} finally {
  await fs.rm(tempRoot, { recursive: true, force: true });
}
